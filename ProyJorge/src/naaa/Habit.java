package naaa;

public class Habit {

	private int num;
	private double precio;
	public Habit(int num, double precio) {
		super();
		this.num = num;
		this.precio = precio;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public double getPrecio() {
		return precio;
	}
	public void setPrecio(double precio) {
		this.precio = precio;
	}
	
	
	
}
